c = 0
print("LISTA GERADA COM WHILE")

#Teste Lógico
while c <= 10:
    print(c)
    c += 1

#Iterações
print("LISTA GERADA COM FOR")
for i in range(0, 11):
    print(i)


# Qual é a principal diferença do While e do For
# O While utiliza um teste lógico para controlar quando a repetição tem que parar.
# E o FOR utiliza um intervalo (range) bem definido para a repetição tem que parar.

